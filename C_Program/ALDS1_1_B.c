/*
最大公約数
２つの自然数 x, y を入力とし、それらの最大公約数を求めるプログラムを作成してください。

２つの整数 x と y について、x ÷ d と y ÷ d の余りがともに 0 となる d のうち最大のものを、
x と y の最大公約数（Greatest Common Divisor）と言います。
例えば、35 と14 の最大公約数 gcd (35, 14) は 7 となります。
これは、35 の約数{1, 5, 7, 35}、14 の約数 {1, 2, 7, 14} の公約数 {1, 7} の最大値となります。

入力
x と y が１つの空白区切りで１行に与えられます。

出力
最大公約数を１行に出力してください。

制約
1 ≤ x, y ≤ 109
ヒント
整数 x, y について、x ≥ y ならば x と y の最大公約数は y と x % y の最大公約数に等しい。ここで x % y は x を y で割った余りである。　

入力例 1
147 105
入力例 1 に対する出力
21
*/
#include <stdio.h>

// 最大公約数を計算する関数の宣言
int gcd(int x, int y);

int main() {
    int x, y;
    // 2つの整数を読み込む
    scanf("%d %d", &x, &y);

    // gcd関数を呼び出して最大公約数を計算
    int result = gcd(x, y);
    
    // 結果を出力
    printf("%d\n", result);

    return 0;
}

// ユークリッドの互除法を用いて最大公約数（GCD）を求める関数
// 原理: x ≥ y のとき、gcd(x,y) = gcd(y, x%y)
// y=0 になった時点での x が最大公約数となる
int gcd(int x, int y){
    int tmp;
    
    // x < y の場合は値を入れ替える（x ≥ y に統一）
    if (x < y) {
        tmp = x;
        x = y;
        y = tmp;
    }

    // ベースケース: y が 0 になったら x が最大公約数
    if (y == 0) {
        return x;
    }
    
    // 再帰呼び出し: (y, x%y) の最大公約数を求める
    // 例: gcd(147, 105) → gcd(105, 42) → gcd(42, 21) → gcd(21, 0) = 21
    return gcd(y, x % y);
}
