/*
グラフの表現方法の解説

【隣接リスト（Adjacency List）】
- 各頂点ごとに、隣接する頂点のリストを保持
- メモリ効率が良い（辺の数に比例）
- 全体の辺を調べるときに効率的

例: 有向グラフで 1→2, 1→4 の場合
  隣接リスト[1] = [2, 4]

【隣接行列（Adjacency Matrix）】
- n×n の行列 A で、A[i][j] = 1 なら i→j の辺がある
- 2つの頂点が辺でつながっているか高速に判定可能
- メモリ効率は悪い（O(n²)）

【問題の要点】
隣接リスト形式の入力を隣接行列に変換

入力形式:
u k v1 v2 ... vk
  → u: 頂点番号
  → k: 出次数（隣接する頂点の個数）
  → v1, v2, ..., vk: 隣接頂点のリスト

【具体例】
入力:
4
1 2 2 4     ← 頂点1から頂点2,4へ辺がある
2 1 4       ← 頂点2から頂点4へ辺がある
3 0         ← 頂点3から出ていく辺なし
4 1 3       ← 頂点4から頂点3へ辺がある

隣接行列:
    1  2  3  4
1 [ 0  1  0  1 ]  ← 1→2, 1→4
2 [ 0  0  0  1 ]  ← 2→4
3 [ 0  0  0  0 ]  ← 出ていく辺なし
4 [ 0  0  1  0 ]  ← 4→3

【実装ステップ】
1. n×n の隣接行列を 0 で初期化
2. n 行分の隣接リスト情報を読み込み
3. 各隣接頂点について A[u][v] = 1 を設定
4. 行列を出力

【計算量】
入力: O(V + E) - V個の頂点、E個の辺
行列出力: O(V²)
総合: O(V + E + V²)
*/

#include <stdio.h>
#include <string.h>

#define MAX_V 101

int main(void) {
    int n;
    scanf("%d", &n);
    
    // 隣接行列を 0 で初期化
    int Adj[MAX_V][MAX_V];
    memset(Adj, 0, sizeof(Adj));
    
    // 隣接リスト形式の入力を読み込む
    for (int i = 0; i < n; i++) {
        int u, k;
        scanf("%d %d", &u, &k);
        
        // 頂点 u に隣接する k 個の頂点を読み込む
        for (int j = 0; j < k; j++) {
            int v;
            scanf("%d", &v);
            // 辺 u→v が存在することを記録
            Adj[u][v] = 1;
        }
    }
    
    // 隣接行列を出力
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            if (j > 1) printf(" ");
            printf("%d", Adj[i][j]);
        }
        printf("\n");
    }
    
    return 0;
}