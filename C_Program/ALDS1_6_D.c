/*
最小コストソートの解説

【問題の理解】
荷物を交換するのにコストがかかる
交換コスト = 2つの荷物の重さの合計

例: 1 5 3 4 2 を昇順にソート

■重さと値の対応
元の位置: [1:0, 5:1, 3:2, 4:3, 2:4]
（値:位置）

■目標位置
1→0, 2→4, 3→2, 4→3, 5→1
（値→目標位置）

■実装のアプローチ
選択ソートを使い、各位置で最小値を探す
交換時、コスト = (現在の値) + (最小値) を加算

【具体例】

位置0: [1, 5, 3, 4, 2]
  最小値は1（既に位置0）→ コスト0

位置1: [1, 5, 3, 4, 2]
  最小値は2（位置4）
  交換: 5 + 2 = 7 → コスト += 7
  結果: [1, 2, 3, 4, 5]

位置2: [1, 2, 3, 4, 5]
  最小値は3（既に位置2）→ コスト0

位置3: [1, 2, 3, 4, 5]
  最小値は4（既に位置3）→ コスト0

位置4: [1, 2, 3, 4, 5]
  最小値は5（既に位置4）→ コスト0

合計コスト: 7 ✓

【別の例】
4 3 2 1

位置0: 最小値1（位置3）
  交換: 4 + 1 = 5 → [1, 3, 2, 4]

位置1: 最小値2（位置2）
  交換: 3 + 2 = 5 → [1, 2, 3, 4]

位置2: 最小値3（位置2）
  スキップ

位置3: 最小値4（位置3）
  スキップ

合計コスト: 5 + 5 = 10 ✓

【計算量】
時間: O(n²) - 選択ソートの形式
空間: O(1) - インプレース処理

【アルゴリズム】
for i = 0 to n-1:
  j = i で最小値を探す
  if (A[j] != A[i]):
    cost += A[i] + A[j]
    swap(A[i], A[j])
*/

#include <stdio.h>

int main(void) {
    int n;
    scanf("%d", &n);
    
    int A[n];
    for (int i = 0; i < n; i++) {
        scanf("%d", &A[i]);
    }
    
    long long cost = 0;  // 総コスト
    
    // 選択ソートで最小交換コストを計算
    for (int i = 0; i < n; i++) {
        // i 以降で最小値を探す
        int minIdx = i;
        for (int j = i + 1; j < n; j++) {
            if (A[j] < A[minIdx]) {
                minIdx = j;
            }
        }
        
        // 最小値が現在位置と異なったら交換
        if (minIdx != i) {
            // 交換コスト = 2つの値の合計
            cost += A[i] + A[minIdx];
            
            // スワップ
            int temp = A[i];
            A[i] = A[minIdx];
            A[minIdx] = temp;
        }
    }
    
    printf("%lld\n", cost);
    
    return 0;
}