/*
素数
約数が 1 とその数自身だけであるような自然数を素数と言います。
例えば、最初の8 つの素数は2, 3, 5, 7, 11, 13, 17, 19 となります。1 は素数ではありません。

n 個の整数を読み込み、それらに含まれる素数の数を出力するプログラムを作成してください。

入力
最初の行に n が与えられます。続く n 行に n 個の整数が与えられます。

出力
入力に含まれる素数の数を１行に出力してください。

制約
1 ≤ n ≤ 10,000
2 ≤ 与えられる整数 ≤ 108
入力例 1
6
2
3
4
5
6
7
入力例 1 に対する出力
4
*/
#include <stdio.h>

// 与えられた整数が素数かどうかを判定する関数
int isPrime(int x);

int main(){
    int n;
    // 入力される整数の個数を読み込む
    scanf("%d", &n);

    // n個の整数を格納する配列を宣言（可変長配列）
    int x[n];
    // n個の整数を1つずつ読み込む
    for(int i = 0; i < n; i++){
        scanf("%d", &x[i]);
    }

    // 素数の個数をカウント
    int count = 0;
    // 各要素が素数かどうかを判定
    for(int i = 0; i < n; i++){
        if(isPrime(x[i])){
            // 素数なら count をインクリメント
            count++;
        }
    }
    // 素数の個数を出力
    printf("%d\n", count);
    return 0;
}

// 素数判定関数
// x が素数なら 1 を、素数でなければ 0 を返す
int isPrime(int x){
    // 2未満の数は素数ではない
    if(x < 2) return 0;
    
    // 2 から √x までの範囲で割り切れるかチェック
    // √x までチェックすれば十分な理由:
    // x が合成数なら、必ず √x 以下の因数を持つため
    // 例: x = 100 の場合、最大で √100 = 10 までチェック
    for(int i = 2; i * i <= x; i++){
        // x を i で割った余りが 0 なら、i は x の因数
        if(x % i == 0){
            // x は合成数（素数ではない）
            return 0;
        }
    }
    // ループ完了：x は 2～√x で割り切れない = 素数
    return 1;
}